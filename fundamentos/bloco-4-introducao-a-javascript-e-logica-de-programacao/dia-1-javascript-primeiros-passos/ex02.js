const a = 7;
const b = 3;

if (a > b) {
  console.log(`${a} é maior que ${b}`);
}
else if(a === b) {
  console.log(`${a} e ${b} são iguais.`)
}
else {
  console.log(`${b} é maior que ${a}`);
}

